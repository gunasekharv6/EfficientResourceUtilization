    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
         int selectedRow = manageFoodbanktbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        }else {
        FoodBank foodbank=(FoodBank) manageFoodbanktbl.getValueAt(selectedRow, 1);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("Update Food Bank", new UpdateFoodBankJPanel(userProcessContainer, ecoSystem, foodbank, userAccount));
        cardLayout.next(userProcessContainer);
        }
         
    }//GEN-LAST:event_updatebtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
         int selectedRow=manageFoodbanktbl.getSelectedRow();
         if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Food Bank to delete");
            return;
        }else{
             FoodBank foodbank=(FoodBank) manageFoodbanktbl.getValueAt(selectedRow, 1);
             cityNetwork.getFoodBankDirectory().getFoodBanks().remove(foodbank);
             JOptionPane.showMessageDialog(null, "Successfully deleted Food Bank ");
      
            populateData();
            
            
         }
    }//GEN-LAST:event_deletebtnActionPerformed


